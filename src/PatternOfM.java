import java.util.ArrayList;
import java.util.Scanner;

public class PatternOfM {

	public static void main(String[] args) {
		
		int i, j, row=6;   
		
		for(i=0; i<row; i++)   
		{   
		 
		for(j=0; j<=i; j++)   
		{   
		  
		System.out.print("* ");   
		}   
		 
		System.out.println();   
		}   
		}   
}


